$(document).ready(function () {
  var itemCount = 0;
  $('.item_add').click(function () {
    itemCount++;
    $('#itemCount').html(itemCount).css('display', 'block');
  });

  $('.clear').click(function () {
    itemCount = 0;
    $(".item_add").show();
    $('#itemCount').html('').css('display', 'none');
    $('#priceList .cardPrice').remove();
    $('#productList .cardText').remove();
    $('#total').sum = 0;
  });
});

